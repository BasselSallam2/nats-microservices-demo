import { ActivityService } from "models/activity/activity.service";
import { connectDB } from "@config/DB.config";

const activityService = new ActivityService();
await connectDB();
console.log(`connect successfully to DB`);

function scheduleActivityLogCreation() {
  let currentIndex = 0;
  const intervalTime = 30000;

  console.log(`Scheduling activity log creation: one entry every ${intervalTime / 1000} seconds.`);

  const intervalId = setInterval(async () => {
    if (currentIndex >= testData.length) {
      clearInterval(intervalId);
      console.log("âœ… All test data items have been processed and logged.");
      return;
    }

    const currentItem = testData[currentIndex];

    console.log(`--- Processing item ${currentIndex + 1}/${testData.length} ---`);
    console.log(`Event: ${currentItem.eventType}`);

    try {
      await activityService.createOne(currentItem);
      console.log(`Successfully created log for ${currentItem.eventType}.`);
    } catch (error) {
      console.error(`Error creating activity log for ${currentItem.eventType}:`, error.message);
    }
    currentIndex++;
  }, intervalTime);
}

scheduleActivityLogCreation();